
public class VarEjem {
    public static void main(String[] args) {
        var numEntero = 10;
        System.out.println("Entero: " + numEntero);
        
        var numDouble = 10.0;
        System.out.println("double: " + numDouble);
        
        var numeroFloat = 10.0F; //Para que la cree en tipo float
        System.out.println("float: " + numeroFloat);
    }
}
