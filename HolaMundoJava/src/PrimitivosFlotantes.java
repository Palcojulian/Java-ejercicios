
public class PrimitivosFlotantes {

    public static void main(String[] args) {
        /*
            Tipos primitivos de tipo flotante: float, double
         */

        //float
        float miFloat = (float) 2.5;
        System.out.println("Numero float: " + miFloat);
        System.out.println("Min: " + Float.MAX_VALUE);
        System.out.println("Max: " + Float.MIN_VALUE);

        //double
        double miDouble = 10;
        System.out.println("Numero double: " + miDouble);
        System.out.println("Max: " + Double.MAX_VALUE);
        System.out.println("Min: " + Double.MIN_VALUE);
        
        
    }
}
